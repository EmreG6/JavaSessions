package day03_IncrementDecrement;

public class C04_Increment {

	public static void main(String[] args) {
		int numA=2;
		numA = numA+3; // acemi iþi
		System.out.println(numA);
		
		numA+=3; //numA artýk 5
		System.out.println(numA);
		
		int numB=10;
		numB=numB*7; // acemi iþi
		System.out.println(numB); //70
		
		numB*=7;
		System.out.println(numB);
		
		int numC=7;
		numC++;
		System.out.println(numC);
		
		int numD=11;
		numD++;
		System.out.println(numD);
		
		
		
		
	}

}
