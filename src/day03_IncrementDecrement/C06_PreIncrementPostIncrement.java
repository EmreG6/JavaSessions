package day03_IncrementDecrement;

public class C06_PreIncrementPostIncrement {
	public static void main(String[] args) {
		int sayi=10;
		sayi++; //11
		sayi++; //12
		sayi++; //13
		
		System.out.println(sayi); //13
		
		System.out.println(sayi++); //13 bu satýrda iki iþlem var
									//bir artýrma, iki yazdýrma
		
		System.out.println(sayi); //14
		
		//önce artýrmak sonra yazdýrmak isterseniz ++sayi
		//önce yazdýrmak sonra artýrmak isterseniz sayi++
		
		System.out.println(++sayi); //15
		
		
		
		
		
		
		

	}

}


