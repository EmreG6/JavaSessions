package day16_ForLoop;

public class C05_ForLoop {

	public static void main(String[] args) {
		// Soru 3) 100’den baslayarak 50’ye kadar olan sayilari aralarinda virgule olarak ayni satirda
		//         yazdirin
		
		for (int i = 100; i > 50; i--) {
			System.out.print(i +", ");
			
		}
			System.out.print(50);

	}

}
